import java.util.*;

public class CountVowels{
  public static void main(String[] args) {
    int day = 4;
    String s="hello";
    switch (day) {
      case 6 :
        System.out.println("Today is Saturday");
        break;
      case 7:
        System.out.println("Today is Sunday");
        break;
      default:
        System.out.println(s.length());
    }    
  }
}


	        
        	
               
